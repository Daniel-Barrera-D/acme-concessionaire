import mongoose from "mongoose"

const clientSchema = new mongoose.Schema({
    dni: {
        type: String,
        required: true,
        trim: true,
        unique: true
    },
    name: {
        type: String,
        required: true,
        trim: true
    },
    lastname: {
        type: String,
        required: true,
        trim: true
    },
})

export default mongoose.model('Client', clientSchema)